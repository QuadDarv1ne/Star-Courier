def get_art(art_name: str) -> str:
    """Получить ASCII-арт по имени"""
    arts = {
        "eleia": ART_ELEIA,
        "artifact": ART_ARTIFACT,
        "station": ART_STATION,
        "warning": ART_WARNING,
        "sabotage": ART_SABOTAGE,
    }
    return arts.get(art_name, "")


def print_art(art_name: str):
    """Вывести ASCII-арт"""
    print(get_art(art_name))
